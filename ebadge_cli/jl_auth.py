# ── Conditional XOR/ADD mixing ──────────────────────────────

def _cond_mix(state: List[int], key_block: List[int], mask: int, phase: int) -> List[int]:
    result = list(state)
    for i in range(16):
        bit_set = ((1 << i) & mask) != 0
        if phase == 3:
            if bit_set:
                result[i] = result[i] ^ key_block[i]
            else:
                result[i] = (key_block[i] + result[i]) & 0xFF
        else:
            if bit_set:
                result[i] = (key_block[i] + result[i]) & 0xFF
            else:
                result[i] = result[i] ^ key_block[i]
    return result


# ── S-box substitution ──────────────────────────────────────

def _sbox_sub(state: List[int]) -> List[int]:
    result = list(state)
    for pos in (0, 3, 4, 7, 8, 11, 12, 15):
        result[pos] = SBOX[result[pos]]
    for pos in (1, 2, 5, 6, 9, 10, 13, 14):
        result[pos] = ISBOX[result[pos]]
    return result


# ── Block cipher (sub_11b8) ─────────────────────────────────

def _block_cipher(state_in: List[int], ek: List[int], mode: int) -> List[int]:
    s = list(state_in)
    initial = list(state_in)

    # x9=0: Phase3 + Sbox + Phase5 (before any Fibonacci)
    s = _cond_mix(s, ek[0:16], _MASK, 3)
    s = _sbox_sub(s)
    s = _cond_mix(s, ek[16:32], _MASK, 5)

    for x9 in range(1, 9):
        s = _fibonacci_mix(s)
        ek_off = x9 * 0x20

        if x9 == 8:
            # Final mixing
            s = _cond_mix(s, ek[0x100:0x110], _MASK, 3)
            break

        # Phase 2 at x9==2 when mode!=0
        if mode != 0 and x9 == 2:
            for i in range(16):
                bit_set = ((1 << i) & _MASK) != 0
                if bit_set:
                    s[i] = s[i] ^ initial[i]
                else:
                    s[i] = (initial[i] + s[i]) & 0xFF

        s = _cond_mix(s, ek[ek_off:ek_off + 16], _MASK, 3)
        s = _sbox_sub(s)
        s = _cond_mix(s, ek[ek_off + 16:ek_off + 32], _MASK, 5)

    return s


# ── function_E1test (0xacc) ─────────────────────────────────

def function_e1_test(key6: List[int], input16: List[int], seed16: List[int]) -> List[int]:
    expanded_key = [key6[i % 6] for i in range(16)]
    output = list(input16[:16])

    ks1 = _key_schedule(list(seed16[:16]))
    cipher_out = _block_cipher(output, ks1, 0)

    for i in range(16):
        cipher_out[i] = (expanded_key[i] + (cipher_out[i] ^ input16[i])) & 0xFF

    obf = [
        (seed16[0] - 0x17) & 0xFF, (seed16[1] ^ 0xE5),
        (seed16[2] - 0x21) & 0xFF, (seed16[3] ^ 0xC1),
        (seed16[4] - 0x4D) & 0xFF, (seed16[5] ^ 0xA7),
        (seed16[6] - 0x6B) & 0xFF, (seed16[7] ^ 0x83),
        (seed16[8] ^ 0xE9), (seed16[9] - 0x1B) & 0xFF,
        (seed16[10] ^ 0xDF), (seed16[11] - 0x3F) & 0xFF,
        (seed16[12] ^ 0xB3), (seed16[13] - 0x59) & 0xFF,
        (seed16[14] ^ 0x95), (seed16[15] - 0x7D) & 0xFF,
    ]

    ks2 = _key_schedule(obf)
    return _block_cipher(cipher_out, ks2, 1)


# ── Public API ──────────────────────────────────────────────

def get_random_auth_data() -> bytes:
    """Build a [0x00, rand*16] auth init packet."""
    return bytes([0x00]) + os.urandom(16)


def get_encrypted_auth_data(device_data_17: bytes) -> bytes:
    """Encrypt a device challenge [0x00, challenge*16] -> [0x01, encrypted*16]."""
    input16 = list(device_data_17[1:17])
    encrypted = function_e1_test(MAGIC, input16, STATIC_KEY)
    return bytes([0x01] + encrypted)
