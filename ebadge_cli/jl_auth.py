# ── Key schedule (sub_1038) ─────────────────────────────────

def _key_schedule(data16: List[int]) -> List[int]:
    out = [0] * 272
    for i in range(16):
        out[i] = data16[i]

    buf = list(data16[:16])
    checksum = 0
    for b in data16:
        checksum ^= b
    buf.append(checksum & 0xFF)

    for rnd in range(16):
        for i in range(17):
            b = buf[i]
            buf[i] = ((b << 3) | (b >> 5)) & 0xFF
        read_pos = (rnd + 1) % 17
        for j in range(16):
            src = buf[read_pos]
            tbl_idx = 0x0F + rnd * 16 - j
            out[16 + rnd * 16 + j] = (KS_TABLE[tbl_idx] + src) & 0xFF
            read_pos += 1
            if read_pos > 16:
                read_pos = 0
    return out


